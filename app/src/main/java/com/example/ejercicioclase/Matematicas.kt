package com.example.ejercicioclase

class Matematicas {
    fun  sumar(n1:Int,n2:Int):Int{
         return n1+n2
     }

    fun resta(n1:Int,n2:Int): Int {
        return n1-n2
    }

    fun multiplicar(n1:Double,n2:Double):Double{
        return n1*n2
    }

    fun division(n1:Double,n2:Double):Double{
        return n1/n2
    }

}